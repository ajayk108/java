
public class positive_even {

	public static void main(String[] args) {
		
		int x = 8;  
		if(x>0)
		{
			if(x%2==0)
			{
				System.out.println("Given Number is Positive and Even");
			}
			else
			{
				System.out.println("Given Number is Positive but Not Even");
			}
		}
		else
		{
			System.out.println("Given Number is not Positive Number");
		}
	}

}
